NumberInRange = int(input("Enter number in the range of 1 to 7"))
if NumberInRange == 1:
    print("Monday")
elif NumberInRange == 2:
    print("Tuesday")
elif NumberInRange == 3:
    print("Wednesday")
elif NumberInRange == 4:
    print("Thursday")
elif NumberInRange == 5:
    print("Friday")
elif NumberInRange == 6:
    print("Saturday")
elif NumberInRange == 7:
    print("Sunday")
else:
    print("Error")


